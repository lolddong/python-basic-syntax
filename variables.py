# 이 #표시는 프로그래밍에서 주석이라 말한다.
# 주석은 파이썬의 인터프이터가 인식하지 못하도록 쓰는 기호
# 단축키는 Ctrl + /

# 아래 a = 1 의 의미는 a와 1이 같다라는 의미가 아니라,
# a라는 이름의 변수에 1을 담겠다는 뜻이다.
a = 1
b = '1'

# print는 실행 후 결과값을 가시적으로 보여주기 위해 터미널 창에 출력하는 것
print(a)
print(b)

# 아래 뜻은 '새로운 a' 값에 "'원래 a' 값 더하기 1" 값을 넣겠다는 뜻이다
a = a + 1
print(a)

# 변수 자료형 출력해보기  
print(type(a))
print(type(b))

c = 2.1
print(type(c))
